(
    TRADEID INT NOT NULL,
    IS_OFFER BOOLEAN NOT NULL,
    ITEM_NAME VARCHAR(40) NOT NULL,
    ITEM_QUANTITY INT NOT NULL,
    MESSAGE VARCHAR(300) NOT NULL,
    CONSTRAINT PK_TRADE_ID PRIMARY KEY (TRADEID)
);

